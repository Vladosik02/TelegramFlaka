def days_word(n: int) -> str:
    """1 день / 2 дні / 5 днів"""
    n = abs(n) % 100
    if 11 <= n <= 19:
        return "днів"
    r = n % 10
    if r == 1:
        return "день"
    if 2 <= r <= 4:
        return "дні"
    return "днів"


def workouts_word(n: int) -> str:
    """1 тренування / 2 тренування / 5 тренувань"""
    n = abs(n) % 100
    if 11 <= n <= 19:
        return "тренувань"
    r = n % 10
    if r == 1:
        return "тренування"
    if 2 <= r <= 4:
        return "тренування"
    return "тренувань"


