def days_word(n: int) -> str:
    """1 день / 2 дня / 5 дней"""
    n = abs(n) % 100
    if 11 <= n <= 19:
        return "дней"
    r = n % 10
    if r == 1:
        return "день"
    if 2 <= r <= 4:
        return "дня"
    return "дней"


def workouts_word(n: int) -> str:
    """1 тренировка / 2 тренировки / 5 тренировок"""
    n = abs(n) % 100
    if 11 <= n <= 19:
        return "тренировок"
    r = n % 10
    if r == 1:
        return "тренировка"
    if 2 <= r <= 4:
        return "тренировки"
    return "тренировок"


