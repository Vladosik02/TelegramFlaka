"""
ai/hallucination_rules.py — Data-driven правила детектора галлюцинаций.

Используется в `ai/client.py:_detect_hallucination`. Каждое правило описывает:
  • какой write-tool ожидается;
  • regex-паттерны для сообщения пользователя (прямой триггер);
  • (опц.) гибрид: бот сказал «записал», а пользователь упомянул объект (косвенный триггер).

Архитектурное правило: добавление поддержки нового write-tool = новая запись
в `TOOL_DETECTION_RULES`. Сам детектор остаётся неизменным.

Принцип: лучше false-negative, чем false-positive. Цель — поймать явные
случаи «AI пишет ‘записал’, но tool не вызвал», а не угадывать намерения.

НЕ ПОКРЫТО НАМЕРЕННО:
  • save_episode  — внутренний tool, выбор «что важно» субъективен;
  • award_xp      — внутренний tool, авто-начисляется в tool_executor.save_workout
                    (известная ловушка из CLAUDE.md «Двойное XP»).
"""
from __future__ import annotations

import re
from dataclasses import dataclass, field
from typing import List, Pattern


# ───────────────────────────────────────────────────────────────────────────────
# Утилиты
# ───────────────────────────────────────────────────────────────────────────────

def _re(*patterns: str) -> List[Pattern]:
    """Компилирует список regex с IGNORECASE+UNICODE."""
    return [re.compile(p, re.IGNORECASE | re.UNICODE) for p in patterns]


# Общие паттерны «бот сказал ‘записал’/‘зафиксировал’/...»
RESPONSE_ACKS: List[Pattern] = _re(
    r"\bзаписал[аои]?\b",
    r"\bзафиксировал[аои]?\b",
    r"\bсохранил[аои]?\b",
    r"\bотмет(ил|ила|или)\b",
    r"\bдобавил[аои]?\s+в\s+(базу|учёт|истори\w+)\b",
)


# ───────────────────────────────────────────────────────────────────────────────
# DetectionRule
# ───────────────────────────────────────────────────────────────────────────────

@dataclass(frozen=True)
class DetectionRule:
    """
    Правило детекции одного write-tool.

    matches() возвращает True если:
      (а) хотя бы один из user_patterns срабатывает на user_message_lower; ИЛИ
      (б) response_acks И user_objects оба заданы И оба срабатывают
          (бот сказал «записал», пользователь упоминал объект).
    """
    tool: str
    user_patterns: List[Pattern] = field(default_factory=list)
    user_objects: List[Pattern] = field(default_factory=list)
    use_response_acks: bool = False  # включает гибридный матч через RESPONSE_ACKS

    def matches(self, user_lower: str, response_lower: str) -> bool:
        # (а) прямой матч
        for p in self.user_patterns:
            if p.search(user_lower):
                return True
        # (б) гибридный матч
        if self.use_response_acks and self.user_objects:
            ack_hit = any(ack.search(response_lower) for ack in RESPONSE_ACKS)
            if ack_hit:
                for obj in self.user_objects:
                    if obj.search(user_lower):
                        return True
        return False


