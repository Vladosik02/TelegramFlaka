# ───────────────────────────────────────────────────────────────────────────────
# Public API для детектора
# ───────────────────────────────────────────────────────────────────────────────

def detect_expected_tools(user_message: str, final_text: str) -> List[str]:
    """
    Возвращает список tool-ов, которые СЛЕДОВАЛО бы вызвать
    исходя из user_message + final_text. Пустой список = всё ок.

    Безопасно к None/пустым строкам.
    """
    if not user_message:
        return []
    user_lower = user_message.lower()
    response_lower = (final_text or "").lower()
    expected: List[str] = []
    for rule in TOOL_DETECTION_RULES:
        if rule.matches(user_lower, response_lower):
            expected.append(rule.tool)
    return expected
